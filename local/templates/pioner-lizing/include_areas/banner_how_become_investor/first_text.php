Акционерное общество «Инвестиционная компания «НФК-Сбережения» оказывает брокерские услуги на
								всей территории Российской Федерации как профессионалам, так и начинающим участникам рынка
								ценных бумаг посредством удаленного заключения договоров. Компания предлагает своим клиентам
								различные варианты брокерского обслуживания на фондовом рынке – путем передачи заявок на
								совершение сделок брокеру по телефону или через торговый терминал.